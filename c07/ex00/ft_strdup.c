#include <stdio.h>
#include <stdlib.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}

char	*ft_strdup(char *src)
{
	char	*dup;
	int		i;

	i = 0;
	dup = malloc(1 + ft_strlen(src) * (sizeof(char)+ 1));
	while (src[i])
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}

 int main(void)
 {
     char *dup = ft_strdup("test");
     printf("%s\n", dup);
     free(dup);
     return (0);
 }
